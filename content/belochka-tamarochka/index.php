        <h2 style="margin-bottom:3rem;">
            <span lang="ru">Как Белочка и&nbsp;Тамарочка строили<br>со своим папой дачу</span>
            <span lang="en">How Belochka and Tamarochka were building<br>dacha with their father</span>
        </h2>

        <p>Один раз Тамарочка вечером сказала:</p>

        <p>— Мне хочется на воздух очень.
            <br>— А где он, воздух? — спросила Белочка.
            <br>— На даче, — ответила Тамарочка
        </p>

        <p>
            В эту ночь Белочка в&nbsp;своей кроватке не&nbsp;спала до&nbsp;трёх&nbsp;часов&nbsp;ночи. Раскувыркалась она и&nbsp;на&nbsp;другой день все проснулись и&nbsp;начали делать гимнастику, а&nbsp;потом пошли умываться к&nbsp;роднику. Умылись и&nbsp;кушать пошли&nbsp;— мама сварила им&nbsp;курицу с&nbsp;мясом и&nbsp;с&nbsp;картошечкой. Тамарочка так&nbsp;обрадовалась, что&nbsp;не&nbsp;выдержала, упала с&nbsp;ног и&nbsp;ушиблась. А&nbsp;Белочка смеётся над&nbsp;Тамарочкой. Но&nbsp;Тамарочка встала, одернулась и&nbsp;дала кулаком Белочке&nbsp;— Белочка тоже упала. Тут мама вошла в&nbsp;комнату:
        </p>

        <p>— Так, девочки, давайте кушать, а&nbsp;я&nbsp;пока позвоню, чтобы за&nbsp;нами приехала машина. Они очень сладко покушали, как вдруг зазвонил дверной звонок: <span class="nobr">«Динь-динь!»</span></p>

        <p>— Девочки, вы&nbsp;пока оденьтесь, а&nbsp;я&nbsp;открою дверь,&nbsp;— сообщила&nbsp;мама.</p>

        <p>Она пошла открывать дверь.</p>

        <p>— Здравствуйте!
            <br>— Здравствуйте.
            <br>— Вы нас ждали?
            <br>— Да, — ответила мама, — ждали.
            <br>Из-за двери послышался голос Тамарочки.
            <br>— Ну, что, приехали? — спросила&nbsp;она.
            <br>— Да, приехали,&nbsp;— ответила ей&nbsp;мама,&nbsp;— приехали, доченька.</p>

        <p>Через двадцать минут они&nbsp;уже&nbsp;были на&nbsp;даче. На&nbsp;одной половинке участка они&nbsp;позавтракали, и&nbsp;потом принялись за&nbsp;работу. Начали прибивать доски.</p>

        <p>— А можно прибивать доски без&nbsp;гвоздей?&nbsp;— спросила маленькая Белочка
            <br>— Нельзя, а&nbsp;с&nbsp;гвоздями&nbsp;— можно.
            <br>— Но досочкам же&nbsp;больно, они же&nbsp;— деревья,&nbsp;— ответила Белочка и&nbsp;ей&nbsp;стало немного грустно,&nbsp;— а&nbsp;без&nbsp;них&nbsp;— <span class="nobr">точно-точно</span> нельзя?
            <br>— Без них нельзя.
            <br>— А с гвоздиками, значит, можно?
            <br>— Можно.
            <br>— А без гвоздиков?
            <br>— Боже ты мой!</p>

        <p>Сегодня уже дача была готова&nbsp;— жили&nbsp;они&nbsp;там поживали и&nbsp;однажды <span class="nobr">как-то</span> утром папа пообещал в&nbsp;сентябре поехать в&nbsp;лес за&nbsp;грибами. Белочка обрадовалась и&nbsp;Тамарочка тоже&nbsp;обрадовалась, но&nbsp;место в&nbsp;машине было только&nbsp;одно.
            <br>— Я поеду, — сказала Белочка, — я&nbsp;младше!
            <br>— Нет, я&nbsp;поеду, ответила ей&nbsp;Тамарочка, — я&nbsp;взрослее! А&nbsp;нас с&nbsp;детства учили, что&nbsp;маленькие должны всегда уступать взрослым места в&nbsp;транспорте, поэтому ты&nbsp;должна уступить и&nbsp;мне место в&nbsp;машине.</p>

        <p>И они снова подрались. Никак не&nbsp;хотела Белочка отпускать Тамарочкино платье, а&nbsp;Тамарочка схватилась крепко в&nbsp;Белочкино рыженькое платьеце и&nbsp;тоже не&nbsp;хотела его&nbsp;отпускать, так и&nbsp;стояли, друг на&nbsp;друга смотрели. На крик вбежал папа в&nbsp;комнату.</p>

        <p>— Никуда вы обе не&nbsp;поедете, раз вести не&nbsp;умеете, я&nbsp;поеду вдвоем с&nbsp;мамой&nbsp;— и&nbsp;он&nbsp;вышел решительно во&nbsp;двор, завел машину и&nbsp;уехал с&nbsp;мамой в&nbsp;лес за&nbsp;грибами.</p>

        <p>Остались Белочка с&nbsp;Тамарочкой вдвоем и&nbsp;тут&nbsp;же&nbsp;помирились. Но делать нечего&nbsp;— надо было <span class="nobr">как-то</span> догонять папу, ведь без&nbsp;родителей одним дома очень тоскливо. Тогда Белочка взяла белый мел и&nbsp;начирикала на&nbsp;полу в&nbsp;столовой большую ракету. Красивая получилась ракета, большая!</p>

        <p>— Садись, Тамарочка, для тебя у&nbsp;меня в&nbsp;ракете всегда места хватит, — мы&nbsp;с&nbsp;тобой полетим за&nbsp;папой и&nbsp;мамой в&nbsp;лес.</p>

        <p>На ракете было нарисовано два кружочка&nbsp;— два окошка. В&nbsp;один, тот что был&nbsp;поменьше, села Белочка, а&nbsp;в&nbsp;другой, чуть побольше&nbsp;— Тамарочка. Белочка нажала на&nbsp;кнопку и&nbsp;ракета <span class="nobr">быстро-быстро</span> полетела над&nbsp;полями и&nbsp;лугами, догонять папину машину. Они пролетели всего несколько минут, а&nbsp;Солнце уже стало <span class="nobr">большим-большим</span> и&nbsp;оно стало светить <span class="nobr">ярко-ярко</span>&nbsp;— еще&nbsp;бы, ведь ракета летела <span class="nobr">быстро-быстро</span>, намного быстрее автомобиля.</p>

        <p>— Оно такое жгучее! — пожаловалась Белочка, закрываясь рукой от&nbsp;солнца.
            <br>— Да, я&nbsp;не&nbsp;знала, что&nbsp;космонавтов оно так жжет!&nbsp;— с&nbsp;сожалением в&nbsp;голосе проговорила Тамарочка,&nbsp;— бедные космонавты и&nbsp;бедный&nbsp;Гагарин!</p>

        <p class="small">~ 1991</p>

