        <h2>
            <span lang="ru">Анютины глазки</span>
            <span lang="en">Pansy</span>
        </h2>

        <p>Анютины глазки&nbsp;— от слов Анюта (Аня) и&nbsp;глаз (очи). Маленькое растение с&nbsp;указательный палец. Распускается незаметно, каждый пучок дает цветок. А&nbsp;желтые цветочки&nbsp;— это класс. Хорошо сажать под&nbsp;яблонями. <span class="nobr">По-английски</span> это будет “Anute&nbsp;— yuses”. Сажают кучами, кругами. Невыносливые к&nbsp;вытаптыванию.</p>

        <h2>
            <span lang="ru">Космея</span>
            <span lang="en">Cosmos</span>
        </h2>

        <p>
            Космос&nbsp;— среднее растение между мал. и&nbsp;бол.&nbsp;— гибриды&nbsp;же&nbsp;всегда розовые. А&nbsp;простые&nbsp;— все оттенки красного, и&nbsp;есть фиолетовые&nbsp;— только один. Они не&nbsp;должны быть толстые&nbsp;— это болезнь «толстоножка». Цветут в&nbsp;июле, в&nbsp;августе. Космос сажаются в&nbsp;<span class="nobr">апреле-мае</span>. Космос не&nbsp;дарят. Чувствительны к&nbsp;морозам, заморозкам на&nbsp;почве –5&deg;. Выносливы к&nbsp;пересадке с&nbsp;мокрого места на&nbsp;сухое и&nbsp;тут&nbsp;же&nbsp;распускаются. Завертывать к&nbsp;пересадке в&nbsp;газету на&nbsp;балконе. Лучше сажать в&nbsp;горшок. <span class="nobr">По-английски</span> это “Cosmozoigs”.
        </p>

    </section>

